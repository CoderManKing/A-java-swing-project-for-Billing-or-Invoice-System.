    private void faturaTablosunuGuncelle(){
        try{
            List<Fatura> faturaListesi = FaturaDao.faturaListele(user.getId());
            faturaTableModel.setRowCount(0);
            
            for(Fatura fatura: faturaListesi){
                Object[] row = {fatura.getFatura_id(), fatura.getMusteri_id(), fatura.getToplam_tutar()};
                faturaTableModel.addRow(row);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Fatura tablosu getirilemedi.", "uyarı!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void musteriTablosunuGuncelle(){
        try{
            List<Musteri> musteriListesi = MusteriDao.musteriListele(user.getId());
            musteriTableModel.setRowCount(0); //Mevcut satır sayısı 0 yapılır.
            
            for(Musteri musteri: musteriListesi){
                Object[] row = {musteri.getMusteri_id(), musteri.getName(), musteri.getMail()};
                musteriTableModel.addRow(row);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Müşteri tablosu getirilemedi.", "uyarı!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void urunTablosuGuncelle(){
        try{
            List<Urun> urunListesi = UrunDao.urunListele(user.getId());
            urunTableModel.setRowCount(0);
            
            for(Urun urun: urunListesi){
                Object[] row = {urun.getUrun_id(), urun.getUrun_name(), urun.getFiyat()};
                urunTableModel.addRow(row);
            }   
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "Ürün tablosu getirilemedi", "uyarı", JOptionPane.ERROR_MESSAGE);
        }
    }

