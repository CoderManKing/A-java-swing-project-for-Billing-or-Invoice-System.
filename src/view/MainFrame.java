    private void copy_musteri_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_copy_musteri_itemActionPerformed
            int selectedRow = tbl_musteri.getSelectedRow();
            if (selectedRow != -1) {
                StringBuilder stringBuilder = new StringBuilder();
                int columnCount = musteriTableModel.getColumnCount();
                for (int i = 0; i < columnCount; i++) {
                    stringBuilder.append(musteriTableModel.getValueAt(selectedRow, i).toString()).append(",");
                }
                StringSelection stringSelection = new StringSelection(stringBuilder.toString());
                Toolkit.getDefaultToolkit().getSystemClipboard().setContents(stringSelection, null);
                JOptionPane.showMessageDialog(MainFrame.this, "Satır bilgileri kopyalandı!");
            } else {
                JOptionPane.showMessageDialog(this, "Lütfen kopyalamak istediğiniz satırı seçin.", "Uyarı", JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_copy_musteri_itemActionPerformed

